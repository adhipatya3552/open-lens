import { Fragment } from "react";
import { Dialog, Transition } from "@headlessui/react";
import { MediaItem } from "../../../types/media";

interface ShareModalProps {
  isOpen: boolean;
  onClose: () => void;
  media: MediaItem;
  onShare: (platform: string) => void;
}

export function ShareModal({ isOpen, onClose, media, onShare }: ShareModalProps) {
  const shareOptions = [
    {
      name: "Twitter",
      icon: TwitterIcon,
      color: "bg-[#1DA1F2] hover:bg-[#1a8cd8]",
    },
    {
      name: "Facebook",
      icon: FacebookIcon,
      color: "bg-[#1877F2] hover:bg-[#166fe5]",
    },
    {
      name: "Pinterest",
      icon: PinterestIcon,
      color: "bg-[#E60023] hover:bg-[#d50020]",
    },
    {
      name: "LinkedIn",
      icon: LinkedInIcon,
      color: "bg-[#0A66C2] hover:bg-[#094fb0]",
    },
  ];

  return (
    <Transition show={isOpen} as={Fragment}>
      <Dialog onClose={onClose} className="relative z-50">
        <Transition.Child
          as={Fragment}
          enter="ease-out duration-300"
          enterFrom="opacity-0"
          enterTo="opacity-100"
          leave="ease-in duration-200"
          leaveFrom="opacity-100"
          leaveTo="opacity-0"
        >
          <div className="fixed inset-0 bg-black/50" aria-hidden="true" />
        </Transition.Child>

        <div className="fixed inset-0 overflow-y-auto">
          <div className="flex min-h-full items-center justify-center p-4">
            <Transition.Child
              as={Fragment}
              enter="ease-out duration-300"
              enterFrom="opacity-0 scale-95"
              enterTo="opacity-100 scale-100"
              leave="ease-in duration-200"
              leaveFrom="opacity-100 scale-100"
              leaveTo="opacity-0 scale-95"
            >
              <Dialog.Panel className="w-full max-w-md transform overflow-hidden rounded-2xl bg-white p-6 text-left shadow-xl transition-all dark:bg-gray-800">
                <Dialog.Title className="text-lg font-medium text-gray-900 dark:text-white">
                  Share {media.title}
                </Dialog.Title>

                <div className="mt-4">
                  <div className="grid grid-cols-2 gap-4">
                    {shareOptions.map((option) => (
                      <button
                        key={option.name}
                        onClick={() => {
                          onShare(option.name.toLowerCase());
                          onClose();
                        }}
                        className={`flex items-center justify-center space-x-2 rounded-lg px-4 py-2 text-white ${option.color}`}
                      >
                        <option.icon className="h-5 w-5" />
                        <span>{option.name}</span>
                      </button>
                    ))}
                  </div>

                  <div className="mt-4">
                    <label
                      htmlFor="share-link"
                      className="block text-sm font-medium text-gray-700 dark:text-gray-300"
                    >
                      Direct Link
                    </label>
                    <div className="mt-1 flex">
                      <input
                        type="text"
                        id="share-link"
                        value={window.location.href}
                        readOnly
                        className="flex-1 rounded-l-lg border border-gray-300 px-3 py-2 text-sm dark:border-gray-600 dark:bg-gray-700 dark:text-white"
                      />
                      <button
                        onClick={() => {
                          navigator.clipboard.writeText(window.location.href);
                        }}
                        className="rounded-r-lg border border-l-0 border-gray-300 bg-gray-50 px-4 py-2 text-sm font-medium text-gray-700 hover:bg-gray-100 dark:border-gray-600 dark:bg-gray-600 dark:text-gray-300 dark:hover:bg-gray-500"
                      >
                        Copy
                      </button>
                    </div>
                  </div>
                </div>
              </Dialog.Panel>
            </Transition.Child>
          </div>
        </div>
      </Dialog>
    </Transition>
  );
}

